<!-- Spotlight JavaScript -->
<script>
    function openSpotlightSearch() {
        const search = document.getElementById('spotlightSearch');
        const container = document.getElementById('spotlightSearchContainer');
        const input = document.getElementById('spotlightInput');

        if (search && container) {
            search.classList.remove('hidden');
            setTimeout(() => {
                container.classList.remove('scale-95', 'opacity-0');
                container.classList.add('scale-100', 'opacity-100');
                if (input) input.focus();
            }, 50);
        }
    }

    function closeSpotlightSearch() {
        const search = document.getElementById('spotlightSearch');
        const container = document.getElementById('spotlightSearchContainer');
        const input = document.getElementById('spotlightInput');

        if (search && container) {
            container.classList.remove('scale-100', 'opacity-100');
            container.classList.add('scale-95', 'opacity-0');
            if (input) input.value = '';
            // Reset search view
            document.getElementById('spotlightResultsContainer').classList.add('hidden');
            document.getElementById('spotlightQuickLinks').classList.remove('hidden');
            document.getElementById('spotlightDefaultMessage').classList.remove('hidden');

            setTimeout(() => {
                search.classList.add('hidden');
            }, 250);
        }
    }

    // Bind keyboard shortcut (CMD+K or CTRL+K)
    document.addEventListener('keydown', function(e) {
        if ((e.metaKey || e.ctrlKey) && e.key === 'k') {
            e.preventDefault();
            const search = document.getElementById('spotlightSearch');
            if (search && search.classList.contains('hidden')) {
                openSpotlightSearch();
            } else {
                closeSpotlightSearch();
            }
        }
        if (e.key === 'Escape') {
            closeSpotlightSearch();
        }
    });

    // Handle spotlight close clicking outside
    document.addEventListener('DOMContentLoaded', function() {
        const search = document.getElementById('spotlightSearch');
        if (search) {
            search.addEventListener('click', function(e) {
                if (e.target === search) {
                    closeSpotlightSearch();
                }
            });
        }

        // Spotlight Live Search Logic
        const input = document.getElementById('spotlightInput');
        const list = document.getElementById('spotlightResultsList');
        const resultsContainer = document.getElementById('spotlightResultsContainer');
        const quickLinks = document.getElementById('spotlightQuickLinks');
        const defaultMsg = document.getElementById('spotlightDefaultMessage');

        if (input && list) {
            input.addEventListener('input', function() {
                const query = input.value.trim().toLowerCase();

                if (query.length < 2) {
                    resultsContainer.classList.add('hidden');
                    quickLinks.classList.remove('hidden');
                    defaultMsg.classList.remove('hidden');
                    return;
                }

                // Fetch dynamic search results via AJAX
                fetch(`/hekimler/arama?q=${encodeURIComponent(query)}`)
                    .then(res => res.json())
                    .then(data => {
                        list.innerHTML = '';
                        if (data && data.length > 0) {
                            data.forEach(item => {
                                const el = document.createElement('a');
                                el.href = item.url;
                                el.className = "flex items-center justify-between p-3 rounded-xl border border-slate-100 hover:border-[#C96A2B] hover:bg-[#FFF7ED] transition-all group";
                                el.innerHTML = `
                                    <div class="flex items-center gap-3">
                                        <div class="w-8 h-8 rounded-lg bg-orange-50 border border-orange-100 flex items-center justify-center text-xs font-bold text-[#C96A2B]">
                                            \${item.icon || '👨‍⚕️'}
                                        </div>
                                        <div>
                                            <span class="block text-xs font-bold text-slate-800 font-display">\${item.name}</span>
                                            <span class="block text-[10px] text-slate-400">\${item.subtitle}</span>
                                        </div>
                                    </div>
                                    <svg class="w-3.5 h-3.5 text-slate-400 group-hover:text-[#C96A2B] transition-colors" fill="none" stroke="currentColor" stroke-width="2.5" viewBox="0 0 24 24">
                                        <path stroke-linecap="round" stroke-linejoin="round" d="M8.25 4.5l7.5 7.5-7.5 7.5"></path>
                                    </svg>
                                `;
                                list.appendChild(el);
                            });
                        } else {
                            list.innerHTML = `
                                <div class="text-center py-4 text-xs text-slate-400">
                                    Aramanızla eşleşen hekim, klinik veya branş bulunamadı.
                                </div>
                            `;
                        }

                        quickLinks.classList.add('hidden');
                        defaultMsg.classList.add('hidden');
                        resultsContainer.classList.remove('hidden');
                    })
                    .catch(() => {
                        // Fallback static match logic if route doesn't exist yet or fails
                        const localDatabase = [
                            { name: "Doç. Dr. Canan Dağdeviren", subtitle: "Klinik Sahibi / Kardiyoloji", url: "/hekimler", icon: "👩‍⚕️" },
                            { name: "Uzm. Dr. Özgür Demir", subtitle: "Klinik Üyesi / Dahiliye", url: "/hekimler", icon: "👨‍⚕️" },
                            { name: "Prof. Dr. Ahmet Yılmaz", subtitle: "Davet Bekliyor / Nöroloji", url: "/hekimler", icon: "👨‍⚕️" },
                            { name: "Balkan Klinik", subtitle: "Klinik / İzmir", url: "/hekimler", icon: "🏥" }
                        ];

                        const filtered = localDatabase.filter(item =>
                            item.name.toLowerCase().includes(query) ||
                            item.subtitle.toLowerCase().includes(query)
                        );

                        list.innerHTML = '';
                        if (filtered.length > 0) {
                            filtered.forEach(item => {
                                const el = document.createElement('a');
                                el.href = item.url;
                                el.className = "flex items-center justify-between p-3 rounded-xl border border-slate-100 hover:border-[#C96A2B] hover:bg-[#FFF7ED] transition-all group";
                                el.innerHTML = `
                                    <div class="flex items-center gap-3">
                                        <div class="w-8 h-8 rounded-lg bg-orange-50 border border-orange-100 flex items-center justify-center text-xs font-bold text-[#C96A2B]">
                                            \${item.icon}
                                        </div>
                                        <div>
                                            <span class="block text-xs font-bold text-slate-800 font-display">\${item.name}</span>
                                            <span class="block text-[10px] text-slate-400">\${item.subtitle}</span>
                                        </div>
                                    </div>
                                    <svg class="w-3.5 h-3.5 text-slate-400 group-hover:text-[#C96A2B] transition-colors" fill="none" stroke="currentColor" stroke-width="2.5" viewBox="0 0 24 24">
                                        <path stroke-linecap="round" stroke-linejoin="round" d="M8.25 4.5l7.5 7.5-7.5 7.5"></path>
                                    </svg>
                                `;
                                list.appendChild(el);
                            });
                        } else {
                            list.innerHTML = `
                                <div class="text-center py-4 text-xs text-slate-400">
                                    Aramanızla eşleşen sonuç bulunamadı.
                                </div>
                            `;
                        }
                        quickLinks.classList.add('hidden');
                        defaultMsg.classList.add('hidden');
                        resultsContainer.classList.remove('hidden');
                    });
            });
        }
    });
</script>
